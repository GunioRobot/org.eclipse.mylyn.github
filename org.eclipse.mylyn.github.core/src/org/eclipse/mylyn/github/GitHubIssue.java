package org.eclipse.mylyn.github;

public class GitHubIssue {

	private String number;
	
	private String user;

	private String title;

	private String body;
	
	public String getNumber() {
		return number;
	}

	public String getUser() {
		return user;
	}

	public String getTitle() {
		return title;
	}

	public String getBody() {
		return body;
	}

}
